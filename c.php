<p style="color:white">
Why C?
C programming language is a MUST for students and working professionals to become a great Software Engineer specially when they are working in Software Development Domain.
The first and foremost component is the inclusion of the Header files in a C program.
A header file is a file with extension .h which contains C function declarations and macro definitions to be shared between several source files.
The next part of a C program is to declare the main() function.The next part of any C program is the variable declaration. 
It refers to the variables that are to be used in the function. Please note that in the C program, no variable can be used without being declared.
Also in a C program, the variables are to be declared before any operation in the function.Body of a function in C program, refers to the operations that are performed in the functions.
The last part in any C program is the return statement. The return statement refers to the returning of the values from a function. This return statement and return value depend upon the return type of the function. 
For example, if the return type is void, then there will be no return statement. In any other case, there will be a return statement and the return value will be of the type of the specified return type.

